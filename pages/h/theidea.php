      <h5>Las Escuelas en México</h5>
      <p>No es secreto que el sistema de educación en mexico es MALISIMO... El Sistema Educativo Mexicano es producto de la confluencia de dos corrientes de pensamiento: “el liberalismo mexicano que se reafirma con la Guerra y las Leyes de Reforma de 1859 y 1861, y la revolución mexicana de 1917”; pero bueno, que podemos esperar de un sistema que ha sido el mismo desde la epoca de la revolución industrial, por lo que las escuelas esta hechas con el modelo de 'FABRICA', con la premisa de que los alumnos deben aprender en base a repetición, no a estimulación, los alumnos (MATERIA PRIMA) entran a la escuela (FABRICA) a determinada hora, un SONIDO MUY FUERTE indica la hora de entrada, almuerzo y salida; por lo que al final del periodo sale un alumno (ahora PRODUCTO FINAL) hecho en base a una plantilla o catalogo de productos, en vez de ser un SER-HUMANO con ganas de aprovechar y poner en practica aquellas habilidades que descubrio y desarrollo en la ESCUELA.</p>
      <p>La educación deberia variar en base a los alumnos que se tiene, estos deberian poder elegir las materias que desean tomar, en base a la carrera que desean llevar en un futuro.</p>
      <p>Regresando al tema... la educación al no ser la mas favorable para los jovenes mexicanos, no debe ser un obstaculo para aprender cosas nuevas, todo esta en querer.</p>
